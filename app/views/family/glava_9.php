    <div class="page" >

        <p>
            Наверное, не ошибусь, если скажу, что тема измен в семейных отношениях самая острая. По статистике измена стоит
            на втором месте в качестве причины развода. На первом – финансовые проблемы. Наверняка каждая семья проходила
            период, когда возникали, по крайней мере, некоторые сомнения в верности мужа или жены. Или, как минимум, опасения
            по поводу возможной измены. Даже просто понимание того, что теоретически возможна ситуация, при которой близкий
            человек может совершить супружескую измену, у многих вызывает некоторый страх, неуверенность в себе, а иногда
            даже и панику. И сама по себе возможная угроза этого приводит к различным негативным проявлениям в семейных
            отношениях. Например, ревности или чрезмерного контроля. Не говоря уже о тяжелом внутреннем состоянии человека,
            который опасается возможной измены со стороны близкого человека.
        </p><br>
        <p>
            Как правило, изменой считают определенные действия интимного характера. Однако разные люди по-разному воспринимают
            одни и те же действия. Для кого-то изменой считается исключительно свершившийся половой акт, а для другого сама
            по себе откровенная беседа с представителем противоположного пола на интимную тему может показаться нарушением
            супружеской верности.
        </p><br>
        <p>
            Лично я воспринимаю интимную часть в отношениях, как высшее проявление любви. Это касается не только секса,
            но и любых ласк, в том числе и нежные слова. А сам секс я воспринимаю как определенный духовный акт. Собственно
            и слово «любовь» имеет глубоко духовный смысл. Поэтому для меня это весьма свято. Однако для некоторых людей,
            будь то мужчина или женщина, сам по себе секс воспринимается не более чем физиологическая потребность. Например,
            как необходимость есть и пить. Поэтому такие люди не считают чем-то предосудительным беспорядочные половые связи.
            Вероятность измены с их стороны довольно высока, хотя одновременно с этим они понимают, что окружающие люди
            все-таки будут относиться к ним с осуждением. Поэтому иногда они все-таки стараются придерживаться некоторых
            рамок приличия. Вместе с тем, среди таких людей нередко попадаются  представители с определенным типом личности.
            В психологии он называется промискуитетный. Для таких людей вообще не существует каких-то моральных или этических
            ограничений в этой области. Они считают нормальным даже групповой секс или обмен партнерами, в том числе мужьями
            или женами. И могут легко аргументировать собственной логикой такую свою позицию. В целом это вполне нормальные
            и адекватные люди, за исключением этого небольшого отклонения.
        </p><br>
        <p>
            Мы не будем заострять внимание на изменах, связанных именно с подобными отклонениями в мировоззрении человека.
            Подчеркиваю, именно в мировоззрении. Потому что это все-таки психологическая проблема, а не психическое заболевание.
            В этой книге мы будем исходить из того, что на этапе знакомства и самопрезентации кандидаты с подобными
            склонностями были выявлены и благополучно отсеяны. Нам удалось познакомиться с человеком, у которого вполне
            адекватные взгляды на интимную часть отношений, получилось создать с ним семью и даже родить детей. И сейчас
            мы постараемся проанализировать причины, которые толкают изначально любящих и верных супругов на супружескую
            неверность.
        </p><br>
        <p>
            И еще раз хочу подчеркнуть. Здесь не будем рассматривать случаи постоянных многократных измен с множеством
            различных людей. Потому что причина этого в мировоззрении самого человека, который изначально был склонен к
            изменам. Такую склонность нетрудно выявить еще во время конфетно-букетного периода. И если кто-то принимает
            решение создавать семью с таким человеком в надежде на то, что после свадьбы он или она изменится, то супружеские
            измены будут результатом его выбора. Гораздо важнее понять причины разовой измены. Причем такая измена может
            быть мимолетной, один раз с одним человеком, либо долговременной, затяжной роман на стороне.
        </p><br>
        <p>
            Но для начала предлагаю все-таки определиться в термине «супружеская верность». Верный человек, это человек,
            который не предаст ни при каких обстоятельствах, на которого можно будет положиться в самый трудный период жизни.
            И если вспомнить супружескую клятву, которую иногда произносят молодожены во время бракосочетания, то там есть
            обещание быть верным и в горе, и в радости. Нам всем приходится сталкиваться с разными периодами жизни. Особенно
            в семейной жизни. Болезнь одного из супругов, или, не дай Бог, детей. Потеря близких родственников. В конце
            концов, и от финансовых падений никто не застрахован. Каждая из подобных ситуаций является кризисной для отдельно
            взятого конкретного человека. Но только от того, как в такой ситуации поведет себя муж или жена, зависит, как
            этот человек будет себя чувствовать в этот период. Будет ли он ощущать поддержку и помощь, или почувствует
            себя брошенным.
        </p><br>
        <p>
            Например, рождение ребенка. Период декретного отпуска, особенно первый год жизни малыша, для многих мам является
            серьезным стрессовым испытанием. Если в этот период муж часто отказывает в помощи, более того, начинает
            предъявлять претензии жене по поводу внешнего вида, отсутствия внимания к себе, постоянного плача ребенка,

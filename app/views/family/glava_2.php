    <h1 style="color: #eaeaea">Глава 2. Счастье</h1><br>
    <div class="page" >

        <p>Кто хочет быть счастливым? Глупый вопрос? Наверное, не найдется ни одного человека, который сказал бы, что
            очень любит быть несчастным. Однако далеко не каждый может сказать, что полностью счастлив и не так уж много
            людей могут сказать, что вообще счастливы хоть сколько-нибудь. Проблема в том, что состояние собственного счастья
            многие ставят в зависимость от каких-то внешних факторов. Кому-то зарплаты не хватает, и это лишает его счастья,
            кто-то не может себе позволить автомобиль из салона, и от этого несчастен, а у кого-то личная жизнь никак не
            наладиться и он считает, что это однозначно весомая причина, чтобы быть самым несчастным человеком в мире.
        </p><br>
        <p>Довольно часто приходится слышать и от женщин, и от мужчин о том, что не попадается им человек, который смог
            бы сделать их счастливыми, поэтому они несчастны из-за своего одиночества. Так ведь и не попадется. Даже если
            такой человек периодически будет появляться в вашем окружении, он не задержится надолго рядом с «несчастным»
            человеком. Хотя бы потому, что рядом с несчастным человеком некомфортно находиться. Несчастный человек буквально
            физически излучает это состояние, и постепенно оно начинает передаваться тем, кто находится рядом с ним.
            Кроме того, не стоит свое личное счастье ставить в зависимость от другого человека. Потому что такое счастье
            очень хрупкое и недолговечное. Само по себе оно подобно наркотику. Как только появляется какой-то внешний
            источник счастья, к нему быстро возникает привыкание, и уже кажется, что счастья не хватает. Хотелось бы
            чего-то большего. Соответственно, если ставить свое состояние счастья в зависимость от другого человека,
            то постепенно к этому человеку начинают предъявляться все больше требований, который тот не всегда может выполнить.
            А если тот другой человек тоже рассчитывает на то, что счастливым его сделает кто-то? Получается, я хочу, что
            бы она сделала меня счастливым, и она хочет, чтобы я сделал ее счастливой. Но как, если я сам не знаю, как
            сделать счастливым себя? Ведь если бы я знал, как это сделать, то сам был бы счастлив, независимо от другого
            человека.
        </p><br>
        <p>В юности и ранней молодости, когда я знакомился с девушками, то всегда старался произвести на них хорошее
            впечатление. Особенно если был заинтересован в развитии отношений. Однако, несмотря на то, что явной антипатии
            в мою сторону не демонстрировалось, редко, кто из них воспринимал меня, хотя бы как кандидата на «своего парня».
            Мне так казалось. Мы могли мило общаться, даже хорошо проводить время в компании. Но развития каких-то близких
            отношений не было. Почему-то в основном девушки этого избегали. И я всегда воспринимал это на свой счет, мол,
            дело во мне и однозначно в моей внешности. Тогда я не понимал, что внешняя красота – понятие исключительно
            субъективное, и вкусы со временем могут меняться. В дальнейшем я убедился в этом на собственном опыте. По
            житейской неопытности я считал, что девушки любят только красивых, и это главный и единственный критерий их
            выбора. Из-за этого каждая неудача в попытке начать отношения вселяла в меня новую порцию неуверенности в себе
            и как следствие ощущение того, что я в определенной степени несчастен. Однако все больше обращал внимание на
            то, что другие парни, внешность которых вроде бы ничего особенного не представляла, пользовались большой
            популярностью у тех же девушек. Постепенно я пришел к пониманию, что дело вовсе не во внешности. Несомненно,
            она имеет значение, но есть что-то такое, что является более значимым фактором. Многие называют это харизмой.
            Принято считать, что не все обладают этим качеством. Считается, что это некая одаренность, природный магнетизм.
            И если во мне этого нет, то приобрести харизму практически невозможно. Потом я задумался. А что если это качество
            все-таки возможно не только приобрести, но и развить? И я начал наблюдать за теми, кто пользовался наибольшей
            популярностью у противоположного пола. Как за парнями, так и за девушками. И заметил одну важную характерную
            для всех особенность. Они все буквально светились радостью, счастьем. Причем независимо от ситуации. Может быть,
            в каких-то трудных ситуациях этот свет несколько тускнел, но, тем не менее, он был, не пропадал совсем. Более
            того. У них и в других сферах успехи были получше, чем у меня.
        </p><br>
        <p>И я понял, что именно этого мне и не достает. Внутреннего состояния радости. Я всегда ставил это состояние в
            зависимость от каких-то конкретных событий. Получил что-то желаемое – радость. Не получил желаемого – огорчение.
            А так как получалось, что чаще я все-таки не получал желаемого, то и внутреннее чувство неудовлетворенности
            присутствовало чаще, чем чувство радости. О каком же счастье могла идти речь, когда внутри постоянно растущее
            ощущение неудовлетворенности? Естественно такое состояние неминуемо отражалось и на моей внешности и на моем
            поведении. Внешне я выглядел скорее мрачным и скучным, чем веселым и интересным. А в начале любого знакомства
            общался напряженно и односложно. Позже в процессе общения я мог расслабиться и оживиться. Но впечатление было
            уже сформировано и интерес ко мне потерян. А если это еще происходило в компании, где присутствовали более
            энергичные и веселые люди, то естественно предпочтение отдавалось им, а не мне. <b><i>Никому не нравятся мрачные и
            скучные люди. Но всем нравятся веселые и общительные</i></b>.
        </p><br>
        <p>
            С возрастом я задумался. А что же такое счастье? Однозначно, счастье – это состояние радости и удовлетворенности.
            Но что способствует этому состоянию, и что его поддерживает? Очевидно наличие чего-то. Однако практика показывает,
            что обретение чего-то желаемого, достижение какой-то цели далеко не всегда обеспечивает нас полноценным счастьем
            и тем более не всегда обеспечивает этим счастьем надолго.
        </p><br>
        <p>
            Простой пример из жизни. Есть у меня хороший знакомый. Знаем друг друга еще с дошкольного возраста. Художник
            по образованию, прекрасный резчик по дереву. Правда работает совершенно в другой сфере. Когда он женился, его
            очень тяготило отсутствие постоянного жилья. Он вынужден был переезжать с одной съемной квартиры на другую.
            И у него появилось естественное желание приобрести собственное жилье. Несколько лет он шел к реализации этого
            желания. Наконец приобрел квартиру в новом доме. Хорошая квартира. Внутреннюю отделку он делал сам, с радостью,
            с душой. Был ли он в тот момент счастлив? Наверняка да. Он светился радостью. Однако спустя какое-то время он
            сказал: «Была у меня мечта, иметь собственное жилье. И вот она сбылась. Я живу со своей семьей в своей квартире.
            Но почему-то я не чувствую радости от этого. Чего-то мне по-прежнему не хватает». То есть, когда-то человек
            не чувствовал радости, а следовательно ощущение счастья меркло, из-за того, что ему не хватало конкретной
            собственности, а именно квартиры. Но когда он ее приобрел, то счастье его длилось недолго. Скоро он снова
            перестал ощущать радость жизни, и, следовательно, потерял ощущение счастья. Очевидно, исполнение какого-либо
            желания дает лишь временное ощущение радости. И опыт как минимум половины семей говорит о том же. Ведь не
            из-за большого счастья по статистике половина семейных пар разводятся. И немало семей большую часть совместной
            жизни просто терпят эти отношения, вместо того, чтобы по-настоящему радоваться.
        </p><br>
        <p>
            Долгое время я безуспешно пытался найти какой-то универсальный внешний источник радости. Читал различную литературу
            на эту тему, изучал различные психологические, эзотерические учения. В результате пришел к пониманию того,
            что я вообще не там ищу. Не с той стороны подошел к проблеме. В любой проблеме всегда очень важно правильно
            сформулировать вопрос. Неправильная формулировка уводит нас в сторону от правильного решения. И наоборот,
            правильно поставленный вопрос дает нам прямое указание на правильный ответ. Когда мы задумываемся, где мне
            найти источник счастья, мы начинаем искать его во вне, то есть снаружи. При этом перестаем обращать внимание
            на самого себя, на свой внутренний мир. Попросту забываем себя. В попытке найти счастье снаружи, начинаем цеплять
            на себя чьи-то чужие цели, исполнять чужие желания, ошибочно полагая, что это все наше, и именно это принесет
            мне счастье. Но вспомните свое детство. Или просто понаблюдайте за детьми. Разве ребенку требуется что-то для
            ощущения счастья? Он счастлив сам по себе. И на самом деле какие-то приятные события лишь усиливают его состояние
            радости и счастья, которые в нем и так присутствуют. А вот какие-то неприятные для него события придавливают
            эти гармоничные состояния. Но стоит только его отвлечь от этих событий, как он тут же о них забывает и снова
            радостен и счастлив.
        </p><br>
        <p>
            Кто-то может возразить, что сравнение не вполне корректно, потому что у ребенка нет тех проблем, какие появляются
            у взрослых, у него меньше забот и так далее. Так ведь в этом-то и дело. Именно это и мешает нашему счастью.
            И если подойти к этому вопросу с этой позиции, то проблема решается значительно проще. Важно правильно
            сформулировать вопрос. Если задать его в форме: «Где найти счастье?», то искать его можно всю жизнь, но так
            и не найти. Потому что на самом деле оно внутри нас уже имеется с детства. Но вместо того, что бы его увеличивать,
            взрослея, мы сами заваливаем свое счастье разными проблемами. И хотя в большинстве своем мы считаем, что все
            эти проблемы сами на нас сваливаются, и не мы в этом виноваты, при ближайшем рассмотрении выясняется, что,
            как правило, мы сами все проблемы себе и создаем. Своими действиями или бездействиями.
        </p><br>
        <p>
            А вы попробуйте задать себе другой вопрос: «Что мне мешает быть счастливым?» Но исключительно с точки зрения
            вашего внутреннего состояния. Не ищите причины снаружи, в виде различных внешних проблем, а загляните внутрь
            себя, как бы банально это ни звучало.
        </p><br>
        <p>
            Тут мы рассмотрим некоторые наиболее распространенные внутренние состояния, которые чаще всего и создают нам
            проблемы, лишая тем самым ощущения счастья и радости. И познакомимся с нехитрыми способами избавиться от этих
            состояний.
        </p><br>
        <p>
            Но прежде предлагаю взять на вооружение простую технику, которая называется «Дневник». Постарайтесь взять за
            правило выполнять эту технику каждый день. Лучше всего вечером, перед сном. И уже через пару недель вы почувствуете,
            насколько радостнее становится ваша жизнь, и начнете замечать первые изменения к лучшему.
        </p><br>

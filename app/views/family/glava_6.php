            в большей степени это свойственно мужчинам. Гораздо чаще в таких случаях именно мужчины принимают решение связать
            свою жизнь с женщиной на основе интимной сферы. Красивая женщина, шикарная фигура, созерцание которой вызывает
            мощный гормональный взрыв. Добавить к этому осознание, что именно ему удалось завоевать сердце этой красавицы,
            что он смог оставить позади кучу конкурентов, и вот мужская гордость уже громко кричит: «Только попробуй ее
            потерять и станешь таким же неудачником, как и они!»

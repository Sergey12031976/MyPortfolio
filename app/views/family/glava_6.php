            вырастают и их нужно отпускать в свободную абсолютно самостоятельную жизнь. И вот здесь срабатывает заложенная
            самими родителями мина. До этого момента супруги видели смысл совместной жизни. И их жизнь могла быть даже в
            какой-то степени наполнена приятными событиями, связанными именно с детьми. Но теперь дети вышли из родительского
            дома. И этой связи между супругами не стало. В результате начинаются проблемы. Все чаще вспыхивают ссоры между

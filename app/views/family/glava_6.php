        </p><br>
        <p>
            Все это происходит из-за того, что мужчина не видит и даже не старается увидеть в женщине личность, а лишь смотрит
            на ее красивое тело и думает о реализации своих физиологических потребностей. Потому что секс сделали связующим звеном.
        </p><br>
        <p>
            Обращаюсь к женщинам. Дорогие, прекрасные женщины. Вы дивно сотворены Богом. Но не обесценивайте себя до уровня
            куклы для взрослых дяденек. Несомненно, секс важен в семейных отношениях. Но он должен исходить из уважения к
            вам, а не из мужской похоти. Не торопитесь завоевывать мужчину своим телом. Убедитесь, что вы его интересуете,
            прежде всего, как личность. Что он уважает ваши интересы и желания. Если это так, то ваш первый и даже второй,
            и третий отказ от интима мужчина воспримет вполне нормально. Даже еще больше станет вас уважать. Только о таком
            мужчине можно задуматься, как о возможном кандидате в мужья.
        </p><br>
        <p>
            <i><ins>3. Традиции.</ins></i> Пожалуй, это звено в какой-то степени может претендовать на сколь-либо надежное.
            Но при определенных условиях и до какого-то времени. Традиции в семье могут быть самыми разными. Они так же
            могут быть принесены из родительской семьи одного из супругов или могут возникнуть самостоятельно внутри молодой
            семьи. Традиции могут ограничиваться участием исключительно мужа и жены, в этом случае надежность такого звена
            значительно выше, а могут предусматривать участие и детей. В таком случае возникает некоторая опасность, что
            после выхода детей в самостоятельную жизнь это звено потеряет свою силу. Но не обязательно. Потому что нередки
            случаи, когда семейные традиции настолько привлекательны, интересны, что они стимулируют детей на продолжение
            участия в них, в том числе и привлекать к ним свои семьи.
        </p><br>
        <p>
            Традиция – это некое событие, действие, которое регулярно повторяется с определенной периодичностью. Традиции
            привязываться к каким-то другим конкретным событиям. Например, празднование дня рождения членов семьи на берегу
            озера, или отмечать всем вместе новый год на горно-лыжном курорте. Но так же могут иметь и абсолютно самостоятельную
            регулярность. Ну, например, раз в полгода посещать какой-нибудь новый незнакомый город в своей стране или за
            рубежом. Это уже на что хватит вашей фантазии и ваших возможностей. Есть только одно непременное условие для
            любой семейной традиции. Точнее два. Она обязательно каждый раз должна нести достаточно сильные позитивные эмоции
            каждому из членов семьи. И ни в коем случае не должна превращаться в какое-то обязательное мероприятие. Традиций
            может быть и несколько. Тогда семейная жизнь будет более интересной и насыщенной. И если каждое такое событие
            будет непременно ассоциироваться с позитивными эмоциями, то и сама семья будет ассоциироваться с таким же
            позитивом. И даже если и будут происходить какие-то раздоры внутри, то воспоминания о таких событиях и ожидание
            их будут компенсировать этот негатив.
        </p><br>
        <p>
            Такое звено может ослабнуть только в одном случае, если традиция по какой-то причине потеряет свою актуальность.
            Например, из-за уменьшения количества участников. Попросту говоря, если без детей традиция практически теряет
            свою яркость, а после выхода из семьи дети не стремятся ее продолжать, то в этом случае так же может возникнуть
            в отношениях. Но, как правило, такое звено за долгие годы способно создать в отношениях что-то еще, чего будет
            достаточно для сохранения сколь-либо крепких отношений.
        </p><br>
        <p>
            <i><ins>4. Общие цели.</ins></i> Общие цели всегда скрепляли людей друг с другом. Даже во многих тренингах по
            формированию команды, дружного коллектива, зачастую обращается внимание на общую цель, как на объединяющий фактор.
            Это же не менее актуально и в семейных отношениях. Но есть один нюанс. Когда цель достигнута, это звено перестает
            быть актуальным. Поэтому если мужчина и женщина в своих отношениях ставят какую-то общую цель, то она должна
            быть достаточно долгоиграющей, то есть требовать для ее полной реализации много лет, либо необходимо иметь в
            запасе еще несколько интересных общих целей. Такое звено хорошо тем, что в процессе совместного движения к
            цели возникают много новых позитивных эмоциональных связей между людьми. Очень часто после успешного завершения
            какого-то крупного совместного проекта его участники продолжают тесное общение и даже становятся близкими
            друзьями. Поэтому и в семье, когда муж и жена вместе двигаются к одной общей цели, очень высока вероятность,
            что отношения на этом основании будут становиться крепче. Ну и тем радостнее потом вместе насладиться вкусом
            общей победы.
        </p><br>
        <p>
            Общая цель может быть какой угодно. От приобретения чего-нибудь относительно крупного, например нового автомобиля,
            до создания семейного бизнеса. Пожалуй, идеальным вариантом может быть, когда и муж и жена являются творческими
            личностями и начинают вместе реализовывать свой потенциал в этой сфере.

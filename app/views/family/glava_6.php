            выдержать его охладевшее отношение, но и восстановить фигуру после родов. И вот она снова привлекательная и
            соблазнительная для мужа. Но теперь условия совершенно другие. Ребенок родился. И он требует много внимание
            к себе. Особенно в первые годы своей жизни. У мужчины снова гормональные ураганы при виде соблазнительной жены,
            которой неимоверными усилиями удается поддерживать свою внешность на должном уровне. Но как прежде на активную
            сексуальную жизнь сил ей уже не хватает. Даже какого-либо желания не возникает. Но чтобы не потерять мужа она
            банально предоставляет себя ему. Однозначно радости от этого она не испытывает. Какое-то время мужа этот факт
            особо не беспокоит. Но постепенно для него секс становится такой же однообразной рутиной, как и для его жены.
            И он снова теряет интерес. А чтобы как-то компенсировать эту потерю, либо начинает погружаться в алкоголь,
            сбегать к друзьям, либо опять-таки заводит интрижку на стороне. А ответственность за это пытается переложить

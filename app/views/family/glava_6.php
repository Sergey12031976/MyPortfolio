            прочным, что позволяет сохранять отношения несмотря ни на какие трудности. Но имеются и ложные, которые не
            выдерживают испытания ни обстоятельствами, ни временем. Мы проанализируем, почему эти звенья ложные, и какое
            разрушительное действие они оказывают на нашу жизнь.
        </p><br>
        <p>
            <i><ins>1. Дети.</ins></i> Наверное, одно из наиболее распространенных заблуждений, что общие дети – это то, что способно сохранить
            отношения в семье. Нередко женщины в стремлении удержать мужчину около себя, специально рожают от него ребенка
            или даже не одного в надежде, что именно это как минимум привяжет его к ней, а в идеале вызовет в нем какие-то
            теплые чувства, близкие к любви. Бывает, что это срабатывает. Даже довольно часто. Наверное, именно из-за этого
            сформировалось такое мнение. И в определенном смысле дети могут выступать в качестве связующего звена. Но они
            не способны обеспечить действительно крепкие отношения между мужем и женой. Иначе не было бы столько разводов.
            Но даже в тех семьях, которые сохраняются ради детей, нередко можно услышать фразы от кого-нибудь из супругов,
            что если бы не дети, то давно бы уже развелись. Сам факт того, что мужчина и женщина живут вместе, как одна
            семья, не говорит о том, что между ними хорошие отношения.
        </p><br>
        <p>
            Такое связующее звено не просто слабое. Оно еще и очень опасное для самих детей. Даже если муж и жена живут
